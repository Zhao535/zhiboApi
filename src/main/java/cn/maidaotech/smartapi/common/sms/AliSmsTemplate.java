package cn.maidaotech.smartapi.common.sms;

public class AliSmsTemplate {
    private String key;
    private String cn;
    private String aborad;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getCn() {
        return cn;
    }

    public void setCn(String cn) {
        this.cn = cn;
    }

    public String getAborad() {
        return aborad;
    }

    public void setAborad(String aborad) {
        this.aborad = aborad;
    }

}